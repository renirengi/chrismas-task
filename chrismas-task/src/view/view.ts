abstract class Card{
 
  constructor (num:string, name:string, count:string, year:string, shape:string, color:string, size: string, favorite:booling){
    this.num = num;
    this.name = name;
    this.count = count;
    this.year = year;
    this.shape = shape;
    this.color = color;
    this.size = size;
    this.favorite = favorite;
  }


  
}
